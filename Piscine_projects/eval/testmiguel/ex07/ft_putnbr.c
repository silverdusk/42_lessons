#include <unistd.h>

void	ft_putnbr(int nb)
{
	if (nb >= -2147483648 && nb <= 2147483647)
	{
		if (nb < 0)
		{
			write (1, "-", 1);
			nb = -nb;
		}
		if (nb >= 10)
		{
			ft_putnbr (nb / 10);
			nb = nb % 10;
		}
		if (nb < 10)
		{
			nb = nb + '0';
			write (1, &nb, 1);
		}
	}
}

int main()
{
	ft_putnbr(2147483647);
}