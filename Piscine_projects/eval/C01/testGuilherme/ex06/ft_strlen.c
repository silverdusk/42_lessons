#include <stdio.h>
#include <unistd.h>

int	ft_strlen(char *str)
{
	int	count;

	count = 0;
	while (str[count] != '\0')
	{
		count++;
	}
	return (count);
}

 int	main(void)
{
	char *str;
	str = "Hello world!";
	printf("%d", ft_strlen(str));
}
